package com.example.assignment.Model;

public class geofencingModel {

    private String id, name, address;
    private int radius;

    public geofencingModel(){}

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getAddress() {
        return address;
    }

    public int getRadius() {
        return radius;
    }
}
